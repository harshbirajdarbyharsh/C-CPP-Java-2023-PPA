#include <stdio.h>

int main() {
    // Declare and initialize variables
    int age = 25;
    char gender = 'M';
    
    // Display the values of variables
    printf("Age: %d\n", age);
    printf("Gender: %c\n", gender);
    
    // Update variables
    age = 30;
    gender = 'F';
    
    // Display updated values
    printf("Updated Age: %d\n", age);
    printf("Updated Gender: %c\n", gender);
    
    return 0;
}
